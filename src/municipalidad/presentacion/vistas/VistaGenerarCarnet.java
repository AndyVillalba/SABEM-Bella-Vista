    @Override
    public void setBeneficiarios(ArrayList<Beneficiario> beneficiarios) {
        
         boolean[] editable = {false,false,true};
        this.getTbCarnets().setDefaultRenderer(Object.class, new Render());
        DefaultTableModel dt = new DefaultTableModel(new String[]{"BENEFICIARIO","DNI","IMPRIMIR"}, 0) {
 
            Class[] types = new Class[]{
                java.lang.Object.class,java.lang.Object.class,java.lang.Boolean.class
            };
 
            @Override
            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }
            
            @Override
            public boolean isCellEditable(int row, int column){
                return editable[column];
            }
        };
        if(beneficiarios.size() > 0){
        for(int i=0; i< beneficiarios.size(); i++){
        Object fila[] = new Object[3];
        
        fila[0] = beneficiarios.get(i).getApellido();
        fila[1] = beneficiarios.get(i).getDni();
        fila[2] = false;
                

        dt.addRow(fila);
        
        }
        
        }
        
        this.getTbCarnets().setModel(dt);
    }
    
    
     private void confirmarImpresion() {
         
            int filasAImprimir = 0;
          for(int i=0;i<this.getTbCarnets().getRowCount();i++)
            {
                if((boolean)this.getTbCarnets().getValueAt(i, 2))
                {
                   filasAImprimir++;
                  this.presentador.seleccionarCarnet((Integer) this.getTbCarnets().getValueAt(i, 1));
                }
            }
         if(filasAImprimir>0)
         {
             this.presentador.confirmarImpresion();
         }
         else
         {
              JOptionPane.showMessageDialog(this, "Seleccione un Beneficiario", "Error", JOptionPane.ERROR_MESSAGE);
         }
           
    }

   
    
    
    
    
    
