    private void rbSeleccionarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbSeleccionarTodoActionPerformed
        if(this.getRbSeleccionarTodo().isSelected())
        {
            for(int i=0;i<this.getTbCarnets().getRowCount();i++)
            {
                this.getTbCarnets().setValueAt(true, i, 2);
            }
        }
        else
        {
             for(int i=0;i<this.getTbCarnets().getRowCount();i++)
            {
                this.getTbCarnets().setValueAt(false, i, 2);
            }
        }
        
        
    }//GEN-LAST:event_rbSeleccionarTodoActionPerformed

